package fun.gen;

import java.util.Objects;
import java.util.Random;
import java.util.function.Consumer;
import java.util.function.Function;
import java.util.function.Predicate;
import java.util.function.Supplier;
import java.util.stream.Stream;

import static java.util.Objects.requireNonNull;

@FunctionalInterface
public interface Gen<O> extends Function<Random, Supplier<O>> {

    /**
     * creates a generator that always produced the same value
     * @param value the value generated
     * @return a constant generator
     * @param <O> the type of the generated value
     */
    static <O> Gen<O> cons(final O value) {
        return seed -> () -> value;
    }

    /**
     * Returns a generator like this one but with values transformed by the specified function
     *
     * @param fn  the map function that transform the generated values
     * @param <P> the type of the new generator
     * @return a new generator
     */
    default <P> Gen<P> map(final Function<O, P> fn) {
        Objects.requireNonNull(fn);
        return seed -> {
            Supplier<O> gen = this.apply(seed);
            return () -> fn.apply(gen.get());
        };
    }

    default Gen<O> peek(final Consumer<O> consumer) {
        return this.map(it -> {
            consumer.accept(it);
            return it;
        });
    }

    /**
     * Also named flatMap or bind.
     * Creates a new generator that passes the result of this generator into function
     * `fn`. `fn` should return a new generator. This allows you to create new
     * generators that depend on the value of other generators.
     *
     * @param fn  the function that creates a new generator from value generated by this generator
     * @param <P> the type of the new generator
     * @return a new generator
     */
    default <P> Gen<P> then(final Function<O, ? extends Gen<P>> fn) {
        Objects.requireNonNull(fn);
        return seed -> fn.apply(this.apply(SplitGen.DEFAULT.apply(seed))
                                    .get())
                         .apply(SplitGen.DEFAULT.apply(seed));
    }

    /**
     * Creates a generator that generates values from this generator that satisfy the given predicate.
     * Care is needed to ensure there is a high chance this generator will satisfy
     * the predicate. By default, `suchThat` will try 100 times to generate a value that
     * satisfies the predicate. If no value passes this predicate after this number
     * of iterations, a runtime exception will be thrown.
     *
     * @param predicate the predicate satisfied by every generated value
     * @return a generator
     */
    default Gen<O> suchThat(final Predicate<O> predicate) {
        return suchThat(requireNonNull(predicate),
                        1000
        );
    }

    /**
     * Creates a generator that generates values from this generator that satisfy the given predicate.
     * Care is needed to ensure there is a high chance this generator will satisfy
     * the predicate. By default, `suchThat` will try specified number of times to generate a value that
     * satisfies the predicate. If no value passes this predicate after this number
     * of iterations, a runtime exception will be thrown.
     *
     * @param predicate the predicate satisfied by every generated value
     * @param tries     the number of tries
     * @return a generator
     */
    default Gen<O> suchThat(final Predicate<O> predicate,
                            final int tries
    ) {
        requireNonNull(predicate);
        if (tries < 0) throw new IllegalArgumentException("tries < 0");
        return r -> {
            Supplier<O> supplier = apply(r);
            return () ->
            {
                requireNonNull(r);
                for (int i = 0; i < tries; i++) {
                    O value = supplier.get();
                    if (predicate.test(value))
                        return value;
                }
                throw new RuntimeException(String.format("Couldn't satisfy such-that predicate after %s tries",
                                                         tries
                ));
            };
        };
    }

    /**
     * Return a supplier of realized values from this generator
     * <p>
     * Note that this function is a dev helper and is not meant to be used
     * to build other generators.
     *
     * @return a supplier of values
     */
    default Supplier<O> sample() {
        return apply(new Random());
    }


    /**
     * Return a stream of size the specified number of elements
     * <p>
     * Note that this function is a dev helper and is not meant to be used
     * to build other generators.
     *
     * @return a finite stream of values
     */
    default Stream<O> sample(int n) {
        return Stream.generate(apply(new Random())).limit(n);
    }

    /**
     * Return a supplier of realized values from this generator and the given seed
     * <p>
     * Note that this function is a dev helper and is not meant to be used
     * to build other generators.
     *
     * @param random the seed of the generator
     * @return a supplier of values
     */
    default Supplier<O> sample(final Random random) {
        return apply(requireNonNull(random));
    }


}
